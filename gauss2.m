Ab(3,:) = Ab(3,:) - (Ab(3,1)/Ab(1,1))*Ab(1,:);

Ab(3,:) = Ab(3,:) - (Ab(3,2)/Ab(2,2))*Ab(2,:);

Ab(1,:)=Ab(1,:)/Ab(1,1);
Ab(2,:)=Ab(2,:)/Ab(2,2);
Ab(3,:)=Ab(3,:)/Ab(3,3);

